package operator;

public class OperatorEx6 {
    public static void main(String[] args) {
        // 대입연산자 : =
        // 복합대입연산자 : +=, -=, *=, /=, %=

        int i = 3;
        i += 5; // i = i + 5; 8
        i -= 2; // i = i - 2; 6
        i *= 3; // i = i * 3; 18

        System.out.println(i);
    }
}
